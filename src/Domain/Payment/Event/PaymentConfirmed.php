<?php

namespace App\Domain\Payment\Event;

use App\Domain\DomainEvent;

class PaymentConfirmed implements DomainEvent
{
    /**
     * @var string
     */
    private $id;

    /**
     * @param string $id
     */
    public function __construct(string $id)
    {
        $this->id = $id;
    }

    /**
     * @return string
     */
    public function getId(): string
    {
        return $this->id;
    }
}